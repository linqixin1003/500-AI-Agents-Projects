CREATE OR REPLACE VIEW nutrition_foods_view AS
SELECT 
    id,
    name_cn,
    name_en,
    category,
    carbs,
    protein,
    fat,
    fiber,
    calories,
    gi_value,
    CASE 
        WHEN gi_value >= 70 THEN '高GI'
        WHEN gi_value >= 55 THEN '中GI'
        WHEN gi_value > 0 THEN '低GI'
        ELSE '无GI'
    END as gi_level
FROM nutrition_foods;